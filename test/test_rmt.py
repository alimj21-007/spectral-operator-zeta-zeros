import numpy as np

# -----------------------------
# تنظیمات
# -----------------------------
np.random.seed(42)   # برای بازتولیدپذیری
N = 100              # اندازه ماتریس GUE
M = 10               # تعداد فاصله‌هایی که چاپ می‌کنیم

# -----------------------------
# تولید ماتریس GUE
# -----------------------------
# ماتریس مختلط با ورودی‌های گاوسی
X = (np.random.randn(N, N) + 1j * np.random.randn(N, N)) / np.sqrt(2.0)
H = (X + X.conj().T) / 2.0  # Hermitian

# -----------------------------
# محاسبه مقادیر ویژه
# -----------------------------
eigs = np.linalg.eigvalsh(H)
eigs = np.sort(eigs)

